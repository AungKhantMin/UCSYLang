class ClassAndField
{
	String parentName;
	int modifier;
	String fieldName;
	String fieldType;
	
	public ClassAndField(String parentName, int modifier, String fieldName,String fieldType)
	{
	
		this.parentName = parentName;
		this.modifier    = modifier;
		this.fieldName  = fieldName;
		this.fieldType  = fieldType;
	}
}